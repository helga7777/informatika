<p><a href="javascript:void(0)" id="faq_clicker1"><span>Делая первые шаги... </span></a><br/></p>
  <div id="faq_hide1" class="o">
	<div class="o">
	<p>Советую проходить в какую–нибудь студию, Вам преподадут основы рисунка, все наглядно объяснят, у Вас появится опыт. Если возможно нет (или желания), займемся самообучением. 
</p>
<p>Начинающему художнику достаточно иметь мягкий и твердый карандаш, кисти беличьи для акварели, кисти щетина #10, #5, #2 для масла. Лично я делаю упор на масло. Для рисования карандашом использую офисную бумагу, акварелью – (в студии) ватман разрезали пополам или на А4, маслом - ДВП, наносила грунт. 
</p>
<p>Если Вы хотите развивать свое мастерство, то можете, как я, завести альбом. Один лист – один день. Я делаю зарисовки того, что интересно, с натуры . Буквально 20–30 мин в день, и через месяц Вы сами можете увидеть результат. 
</p>
<p>"Без мастерства, сколько бы ни было у Вас чувства, о чем бы интересном Вы ни собирались рассказать, все это останется пустым место. Мастерство в создании картины, этюда, пейзажа, натюрморта и т.д. есть соединение воедино элементов живописи: рисунка, светотени, цвета и композиции. Все эти элементы неотделимо слиты." 
</p>
<p>Об этих элементах нужно говорить отдельно, их нельзя объяснить в двух словах. Что же нужно знать и уметь начинающему художнику: видеть трехмерное пространство и суметь отобразить задуманное на чистом листе или холсте. 
</p>
<p>Как это сделать? Выберите предмет, желательно что–нибудь попроще (в студии это будет шар, цилиндр), в домашних условиях это может быть кружка. Закомпануйте так, чтобы предмет не "выпрыгивал" из–за границ картины, но и не был совсем уж маленьким (одним словом, прорабатывайте композицию). 
</p>
<p>А теперь прищурьте глаза так, чтобы было размыто, и определите для себя что темнее, а что светлее (кружка, стол, стена). Придаем форму. Не важно что у Вас в руках: карандаш или кисть, Вы должны следовать форме . Совершенно безразлично будет это маленький штрих или большой мазок кистью. Возьмем пример посложнее: шерсть животного. Вы, не задумываясь, будете рисовать по направлению шерсти. А теперь попробуйте как вздумается, и что же получится? Неведома зверушка!) Так и во всем остальном (натюрморт, портрет, пейзаж). 
</p>
<p>В принципе можно рисовать то что видим, но знание основ перспективы Вам сильно облегчит задачу.
</p>
