<p>Перед тем как приступить к живописи, Вы должны определиться с композицией, сделать пробный схематичный рисунок ни листке, подготовить материалы для живописи маслом. 
</p>
<p>I этап. Подготовка рабочего места. Нужно определиться: на каком материале Вы будете писать (я использую ДВП, грунтую его, даю высохнуть). Раскладываю палитру, в основном те цвета, которые буду использовать (обычно 7-9). необходимы и большие, и маленькие кисти, скипидар (или уайт-спирит, ...), тряпочка (вытирать кисти после скипидара, на кисти не должно быть больше трех цветов - появляется грязь - поэтому можно обмакивать в скипидаре почаще). 
</p>
<p>II этап. Углем (карандашом), не вдаваясь в подробности, одними контурами наносите рисунок на ДВП (холст, ...). Также можно сделать тоновую заливку (я делаю это всегда, так как это помогает мне в дальнейшем легко определить тон каждого объекта). Я использую несколько цветов (можно одним), только чистые краски, без примесей других цветов и белил; смачиваю кисть в скипидаре, и очень жидко быстро прохожусь по все картине (чем-то этот процесс похож на акварель по мокрому). Никакого объема, тени на предмете, Вы, используя 3-5 цветов палитры, должны получить тоновую картину, чтобы она совпадала с натурой (прищурьте глаза!). Дайте высохнуть. 
</p>
<p>III этап. Пишем маслом. Начинаю всегда с большой кисти, прорабатываю одновременно всю картину, смело нанося мазки по форме предмета. Если какой-то участок картины у Вас не получился, то его можно удалить мастихином (тряпочкой), а потом просто написать заново. Я грешу тем, что стараюсь использовать все цвета одновременно, поэтому некоторые картины у меня получаются пестрыми, поэтому сейчас стараюсь делать тонкие переходы от света к тени близкими по спектру цветами. В любом случае, Вы не должны бояться пробовать писать различными техниками, стилями; главное для меня это то, что я получаю огромное удовольствие от живописи. На завершающем этапе я использую кисть #2, подправляя мелкие детали и нанося последние штрихи. Пробуйте и экспериментируйте, и все у Вас получится!
</p>
