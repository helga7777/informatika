 <li>
        
<p><a href="javascript:void(0)" id="faq_clicker2"><span>Я рассказываю о своем опыте в графике </span></a><br/></p>
  <div id="faq_hide2" class="o">
	<div class="o">
<p>Материалы для рисования очень разнообразны. Например, я использую карандаши различной мягкости, цветные карандаши (но Вы можете использовать также пастель, тушь, перо, уголь древесный). Рисую на обыкновенной офисной бумаге (конечно, можно купить специальную бумагу для рисования в магазине). 
</p>
<p>При работе над рисунком, как я уже говорила, очень важно представить бумагу трехмерностью. Рисунок большой глубины (в пейзаже: река на переднем плане, лес - на среднем, горы - на заднем) надо начинать с переднего плана, прорисовать детали если надо, обозначить цветом, контрастностью. 
</p>
<p>Одной из ключевых методик работы над рисунком является целостность в изображении. Запомните! Нужно идти от общему к частному, и при этом все должно быть подчинено одной идее. То есть: к примеру, Вам нужно построить кирпичный дом, и Вы постепенно, кирпичик за кирпичиком, начинаете строительство, при этом в уме Вы держите весь проект дома. Ведь если это не так, у Вас в результате вряд ли получится дом, кирпичная стена в лучшем случае. Также и в рисовании. Это очень важный момент. Нельзя разбить лист на квадраты, а потом просто заполнять их, теряя целостность, приходится подгонять под размеры квадратов, а следовательно делать ошибки. 
</p>
<p>Почему нужно идти от общему к частному? Для того чтобы на листке поместился весь котенок, цветок и т.д., а не потерять потом хвостик, лапку и т.д. из-за отсутствия места на листке. Как это сделать? Это первый этап рисования: после того как Вы определились с объектом рисования, нужно скомпоновать его на листе, провести ось, центр и отметить границы (насколько отступает от всех границ листа), далее простыми геометрическими формами обозначаете самые крупные объекты или массы. Для того чтобы определить размеры и пропорции, можно взять карандаш; и, либо на вытянутой руке на каком-то расстоянии от предмета, либо непосредственно на предмете, измерить сколько карандашей помещается по горизонтали, а сколько - по вертикали. Например, вся композиция по ширине в два раза меньше, чем по высоте (1 карандаш - по ширине, 2 карандаша - по высоте), то следует взять лист с книжной ориентацией. 
</p>
<p>2 этап. Более детальная проработка. Сюда входит прорисовка и штрихование. На данном этапе только намечаются тени. Вы анализируете, и по форме штрихом (пятном) обозначаете самое светлое место, и самое темное. Не стоит слишком усердствовать на данном этапе в наборе цвета, здесь можно запутаться. В результате Вам может не хватить выразительных средств, чтобы отобразить самое темное пятно на картине ( У меня бывало такое, перестараешься - и ты уже зачернила рисунок; хотя может быть и наоборот). 
</p>
<p>3 этап. Сам рисунок здесь уже, наверное, поздно менять. На третьем этапе Вы набираете цвет. Я начинаю набирать тон с самого темного места, и относительно него уже весь остальной рисунок. То есть иду от темного к светлому (не забывайте: прорабатывается одновременно весь рисунок!), но постепенно: сначала получается серенький рисунок, без особых контрастов, потом слой за слоем накладываю тон, обозначаю контрасты (отдыхайте от рисования, и щурьте глаза - сначала на натуру, потом переводете взгляд на Ваш рисунок, сравнивается). На завершающем этапе устраняются недочеты, что-то подчеркивается (линия, пятно). 
</p>
<p>Думаю, на этом можно пока закончить. Конечно, стоило бы подкрепить данный материал рисунками, но это уже если будет необходимость и Ваш интерес:)
</p>
